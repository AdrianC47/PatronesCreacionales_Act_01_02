    private void agregarCelcius(){
        String temp = javax.swing.JOptionPane.showInputDialog("Ingrese temperatura en celsius");
        if(temp.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(null, "No se ha ingresado un valor de temperatura", "Temperatura en Celsius", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
        ITemperatureDevice celsiusDevice = new CelsiusDevice(Double.parseDouble(temp));
        controller.addDeviceTemperature(celsiusDevice);
    }
    private void agregarFahrenheit(){
        String temp = javax.swing.JOptionPane.showInputDialog("Ingrese temperatura en celsius");
        if(temp.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(null, "No se ha ingresado un valor de temperatura", "Temperatura en Celsius", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
        FahrenheitDevice fahrenheitDevice = new FahrenheitDevice(Double.parseDouble(temp));
        ITemperatureDevice fahrenheitAdapter = new FahrenheitToCelsiusAdapter(fahrenheitDevice);
        controller.addDeviceTemperature(fahrenheitAdapter);
    }
    
    private void listTemperatures(){
        jTextArea1.setText("");
        List<Double> temperatures = controller.getTemperatures();
        jTextArea1.append("Temperaturas en Celsius:\n");
        for (double temp : temperatures) {
            jTextArea1.append(temp + " Â°C\n");
        }
    }
